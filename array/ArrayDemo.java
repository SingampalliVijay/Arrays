package kn.com.array;

public class ArrayDemo {

	public static void main(String[] args) {
		// Array Declaration
	int []arr;
	//Array Creation
	arr=new int[5];
	//Array initialization
	arr[0]=10;
	arr[1]=20;
	arr[2]=30;
	arr[3]=40;
	arr[4]=50;
	//Array Traversing
	for(int i=0;i<=4;i++) {
		System.out.println(arr[i]);
	}

	}
}
